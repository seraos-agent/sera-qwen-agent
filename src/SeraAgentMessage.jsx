const fmtTime = (ts) => {
  if (!ts) return "";
  const d = new Date(ts * 1000);
  return [d.getHours(), d.getMinutes(), d.getSeconds()]
    .map(n => String(n).padStart(2, "0")).join(":");
};

const groupIntoPhases = (rows) => {
  if (!rows || rows.length === 0) return [];
  const phases = [];
  let currentPhase = null;

  rows.forEach(r => {
    // If it has a title, we group by title. Otherwise group by phase name.
    const groupKey = r.title ? `title_${r.title}` : `phase_${r.phase || "planning"}`;
    const displayName = r.title || r.phase || "planning";

    if (!currentPhase || currentPhase.groupKey !== groupKey) {
      if (currentPhase) phases.push(currentPhase);
      currentPhase = { id: `ph_${phases.length}`, groupKey, name: displayName, logs: [], done: r.done, active: false };
    }
    currentPhase.logs.push(r);
    if (!r.done) currentPhase.done = false;
  });
  if (currentPhase) phases.push(currentPhase);

  const activeIndex = phases.findIndex(p => !p.done);
  if (activeIndex !== -1) phases[activeIndex].active = true;

  phases.forEach((p, i) => {
    if (activeIndex !== -1 && i < activeIndex) p.done = true;
    if (activeIndex === -1) p.done = true;
  });
  return phases;
};

