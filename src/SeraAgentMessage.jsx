// Map phase names to meaningful status messages for the user
const phaseToStatusMsg = (phase, tool) => {
  if (tool) {
    if (tool.includes('batch_create') || tool.includes('publish')) return "Building your storefront";
    if (tool.includes('generate_store_assets') || tool.includes('image')) return "Generating visuals & assets";
    if (tool.includes('get_stores') || tool.includes('find')) return "Fetching store data";
    return `Running ${tool.replace(/_/g, ' ')}`;
  }
  const p = (phase || '').toLowerCase();
  if (p.includes('analy')) return "Analyzing your request";
  if (p.includes('brand') || p.includes('design')) return "Crafting brand identity";
  if (p.includes('catalog') || p.includes('product')) return "Building product catalog";
  if (p.includes('asset') || p.includes('gen') || p.includes('image')) return "Generating visuals";
  if (p.includes('layout') || p.includes('schema')) return "Structuring storefront layout";
  if (p.includes('polic')) return "Defining store policies";
  if (p.includes('execut') || p.includes('tool')) return "Executing operations";
  if (p.includes('verif') || p.includes('valid')) return "Verifying results";
  return "Working on your request";
};

